import React, {useState} from 'react';
import ColorBox from './ColorBox';

function ColorBoxes( { potentialColors }) {
    function randomIndex() {
        return Math.floor(Math.random() * potentialColors.length)
    }

    function genRandomColors() {
        let colorsArray = [];
        for (let i = 0; i < 16; i++) {
            colorsArray.push(potentialColors[randomIndex()])
        }
        return colorsArray;
    }

    const [currentColors, setCurrentColors] = useState(genRandomColors)

    function updateBoxColor() {
        const randomColorIndex = Math.floor(Math.random() * currentColors.length);
        const newColor = potentialColors[randomIndex()];
        setCurrentColors(currentColors.map((element, index) => {
            if(index === randomColorIndex) {
                return newColor;
            } else {
                return element;
            }
        }))
    }

    let boxes = currentColors.map((element, index) => <ColorBox color={element} key={index}/>) 
    return (
    <>
        {boxes}
        <button onClick={updateBoxColor}>Change</button>
    </>
    )
  
}

